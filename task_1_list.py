# list :-

# 1. extend()

list1 = [1, 2, 3]
list2 = [4, 5, 6]
list1.extend(list2)
print(list1)  

# Output: [1, 2, 3, 4, 5, 6]


# 2. index()
 
my_list = [10, 20, 30, 20, 40]
index = my_list.index(20)
print(index)  

# Output: 1

# 3. insert()

my_list = [1, 2, 3, 4]
my_list.insert(1, 5)
print(my_list)  

# Output: [1, 5, 2, 3, 4]


# 4. count()

my_list = [1, 2, 2, 3, 2, 4]
count = my_list.count(2)
print(count)  

# Output: 3

# 5. clear()

my_list = [1, 2, 3]
my_list.clear()
print(my_list)  

# Output: []

	