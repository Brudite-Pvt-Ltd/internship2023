#1. Lists:
ank_list= [1,2,3,4,5]

#1.1. append(): Add an element to the end of the list.
ank_list.append(6)
print(ank_list)

#1.2. pop(): Remove and return the last element from the list.
ank_list.pop()
print(ank_list)

#1.3. insert(): Insert an element at a specified index.
ank_list.insert(2,7)
print(ank_list)

#1.4. reverse(): Reverse the order of elements in the list.
ank_list.reverse()
print(ank_list)

#1.5. count(): Count the occurrences of a specific element in the list.
count=ank_list.count(2)
print(count)








